function smarty_sumstr_semantic($i,&$words,&$fem,$f,$default_lang){
	$_1_2 = $GLOBALS['sumstr_'.$default_lang]['_1_2'];
	$_1_19 = $GLOBALS['sumstr_'.$default_lang]['_1_19'];
	$des = $GLOBALS['sumstr_'.$default_lang]['des'];
	$hang = $GLOBALS['sumstr_'.$default_lang]['hang'];
	$namerub = $GLOBALS['sumstr_'.$default_lang]['namerub'];
	$nametho = $GLOBALS['sumstr_'.$default_lang]['nametho'];
	$namemil = $GLOBALS['sumstr_'.$default_lang]['namemil'];
	$namemrd = $GLOBALS['sumstr_'.$default_lang]['namemrd'];
	$kopeek = $GLOBALS['sumstr_'.$default_lang]['kopeek'];

	$words="";
	$fl=0;
	if($i >= 100){
		$jkl = intval($i / 100);
		$words.=$hang[$jkl];
		$i%=100;
	}
	if($i >= 20){
		$jkl = intval($i / 10);
		$words.=$des[$jkl];
		$i%=10;
		$fl=1;
	}
	switch($i){
		case 1: $fem=1; break;
		case 2:
		case 3:
		case 4: $fem=2; break;
		default: $fem=3; break;
	}
	if( $i ){
		if( $i < 3 && $f > 0 ){
			if ( $f >= 2 ) {
				$words.=$_1_19[$i];
			}
			else {
				$words.=$_1_2[$i];
			}
		}
		else {
			$words.=$_1_19[$i];
		}
	}
}

function smarty_modifier_sumstr($L, $default_lang = 'ru') {
	$_1_2 = $GLOBALS['sumstr_'.$default_lang]['_1_2'];
	$_1_19 = $GLOBALS['sumstr_'.$default_lang]['_1_19'];
	$des = $GLOBALS['sumstr_'.$default_lang]['des'];
	$hang = $GLOBALS['sumstr_'.$default_lang]['hang'];
	$namerub = $GLOBALS['sumstr_'.$default_lang]['namerub'];
	$nametho = $GLOBALS['sumstr_'.$default_lang]['nametho'];
	$namemil = $GLOBALS['sumstr_'.$default_lang]['namemil'];
	$namemrd = $GLOBALS['sumstr_'.$default_lang]['namemrd'];
	$kopeek = $GLOBALS['sumstr_'.$default_lang]['kopeek'];

	$s=" ";
	$s1=" ";
	$s2=" ";
	$L = round($L,2);
	$kop=round( ( $L*100 - intval( $L )*100 ));
	$L=intval($L);
	if($L>=1000000000){
		$many=0;
		smarty_sumstr_semantic(intval($L / 1000000000),$s1,$many,3, $default_lang);
		$s.=$s1.$namemrd[$many];
		$L%=1000000000;
	}

	if($L >= 1000000){
		$many=0;
		smarty_sumstr_semantic(intval($L / 1000000),$s1,$many,2,$default_lang);
		$s.=$s1.$namemil[$many];
		$L%=1000000;
		if($L==0){
			$s.=$namerub[1];
		}
	}

	if($L >= 1000){
		$many=0;
		smarty_sumstr_semantic(intval($L / 1000),$s1,$many,1,$default_lang);
		$s.=$s1.$nametho[$many];
		$L%=1000;
		if($L==0){
			$s.=$namerub[3];
		}
	}

	if($L != 0){
		$many=0;
		smarty_sumstr_semantic($L,$s1,$many,0,$default_lang);
		$s.=$s1.$namerub[$many];
	}

	if($kop > 0){
		$many=0;
		smarty_sumstr_semantic($kop,$s1,$many,1,$default_lang);
		$s.=$s1.$kopeek[$many];
	}
	else {
		$s.=" 00 ".$kopeek[3];
	}

	return my_mb_ucfirst(trim($s));
}

function my_mb_ucfirst($str, $e='utf-8') {
    $fc = mb_strtoupper(mb_substr($str, 0, 1, $e), $e);
    return $fc.mb_substr($str, 1, mb_strlen($str, $e), $e);
}

